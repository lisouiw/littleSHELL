#include "../libft.h"

void	ft_put_rainbow(void)
{
	ft_putstr("\033[31mr\033[0m");
	ft_putstr("\033[33ma\033[0m");
	ft_putstr("\033[32mi\033[0m");
	ft_putstr("\033[36mn\033[0m");
	ft_putstr("\033[34mb\033[0m");
	ft_putstr("\033[35mo\033[0m");
	ft_putstr("\033[37mw\033[0m");
}
