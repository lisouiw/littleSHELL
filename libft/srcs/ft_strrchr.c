#include "../libft.h"

void	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*b;

	i = ft_strlen(s);
	b = (char *)s;
	while (i >= 0 && b[i] != c)
		i--;
	if (b[i] == c)
		return (&b[i]);
	return (NULL);
}
